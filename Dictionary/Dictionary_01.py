''' A Dictionary represents a group of elements in the form of key value.
    Dictionary in python unordered collection
    Syntex --> dict_name = {key1:value1, key2:value2, key3:value3,.......}
'''

stu = {101:'Sarwar', 102:'Mehedi', 103:'Salman', 104:'Abu Taher', 105:'Sajada'}   #Create dictionary key:value
print(stu)

print(stu[101])
print(stu[102])
print(stu[103])
print(stu[104])
print(stu[105])
print()

