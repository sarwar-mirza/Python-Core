''' We can check whether a key is already existing in 
    Dictionay or not, for this purpose we use membership Operator

    101 in stu # if exists returns True
    104 not in stu # if not exists return True
'''

stu = {101:'Liza',102:'Lima',103:'Sarwar mithu'}
print(101 in stu)                                 # Using Membership Operator exists returns True
print(104 in stu)                                 #    ||    ||        ||     Exists returns False
print()


print(10 not in stu)                                 # Using Membership Operator exists returns True
print(102 not in stu)                                 #    ||    ||        ||     Exists returns False
